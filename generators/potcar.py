def _potcar_create() -> None:
    """Create POTCAR by selecting elements and a local POTCAR directory."""
    st.markdown("###### Create POTCAR")
    st.info("POTCAR is generated by concatenating individual POTCAR files for each element.")

    if "potcar_path" not in st.session_state:
        st.session_state.potcar_path = ""

    potcar_path = st.text_input(
        "POTCAR Directory Path",
        value=st.session_state.potcar_path,
        placeholder="/path/to/potcars",
    )

    potcar_elements = st.multiselect("Select Elements for POTCAR", _ALL_ELEMENTS)
    functional = st.selectbox("Exchange-Correlation Functional", ["PBE", "LDA", "GGA"])

    st.session_state.potcar_path = potcar_path

    if st.button("Generate POTCAR"):
        if not potcar_elements:
            st.error("Please select at least one element")
            return
        if not potcar_path:
            st.error("Please specify POTCAR directory path")
            return

        _build_potcar(potcar_path, potcar_elements, functional)


def _build_potcar(potcar_path: str, elements: list, functional: str) -> None:
    """
    Read per-element POTCAR files from disk and concatenate them.

    Tries two directory layouts:
      1. <potcar_path>/<functional>/<element>/POTCAR
      2. <potcar_path>/<element>/POTCAR   (fallback)
    """
    potcar_contents = []
    missing_elements = []

    for elem in elements:
        # Primary path: <root>/<functional>/<elem>/POTCAR
        primary = os.path.join(potcar_path, functional, elem, "POTCAR")
        # Fallback path: <root>/<elem>/POTCAR
        fallback = os.path.join(potcar_path, elem, "POTCAR")

        found = False
        for path in (primary, fallback):
            if os.path.exists(path):
                try:
                    with open(path, "r") as f:
                        potcar_contents.append(f.read())
                    found = True
                    break
                except Exception as e:
                    st.warning(f"Could not read {elem} POTCAR: {e}")

        if not found:
            missing_elements.append(elem)

    # Report missing elements with a helpful concatenation command
    if missing_elements:
        st.error(f"POTCAR files not found for: {', '.join(missing_elements)}")
        _show_cat_command(potcar_path, elements, functional)
        return

    if potcar_contents:
        st.session_state.potcar_content = "".join(potcar_contents)
        st.success(f"POTCAR generated with {len(elements)} element(s)!")
    else:
        st.error("No POTCAR files could be read. Please check the path.")
        _show_cat_command(potcar_path, elements, functional)


def _show_cat_command(potcar_path: str, elements: list, functional: str) -> None:
    """Display a shell command the user can run manually to build POTCAR."""
    cmd = "# Command to concatenate POTCAR files:\ncat "
    for elem in elements:
        cmd += f"{potcar_path}/{functional}/{elem}/POTCAR \\\n"
    cmd += "> POTCAR"
    st.code(cmd, language="bash")
