"""
generators/potcar.py — POTCAR file generator.

Supports uploading an existing POTCAR or creating one by selecting
elements and concatenating per-element POTCAR files from a local directory.
"""

import os

import streamlit as st

from sections.input_generator import render_file_output


# ===========================================================================
# Public entry point
# ===========================================================================

def render_potcar_tab() -> None:
    """Render the POTCAR generator tab."""
    if "potcar_show_save_dialog" not in st.session_state:
        st.session_state.potcar_show_save_dialog = False

    potcar_method = st.radio(
        "Choose generation method:",
        ["Upload POTCAR", "Create POTCAR"],
        horizontal=True,
    )

    handlers = {
        "Upload POTCAR": _potcar_from_upload,
        "Create POTCAR": _potcar_create,
    }
    handler = handlers.get(potcar_method)
    if handler:
        handler()

    # Show preview if content has been generated
    if "potcar_content" in st.session_state:
        render_file_output(st.session_state.potcar_content, "POTCAR", "potcar")


# ===========================================================================
# Upload method
# ===========================================================================

def _potcar_from_upload() -> None:
    """Load POTCAR from an uploaded file."""
    st.markdown("###### Upload Existing POTCAR")
    uploaded_file = st.file_uploader("Choose a POTCAR file", type=None,
                                     accept_multiple_files=False)
    if uploaded_file is not None:
        try:
            file_content = uploaded_file.getvalue().decode("utf-8")
            st.success("File uploaded successfully!")
            if st.button("Use This POTCAR", key="gen_uploaded_potcar"):
                st.session_state.potcar_content = file_content
                st.success("POTCAR loaded successfully!")
                st.rerun()
        except Exception as e:
            st.error(f"Error reading file: {str(e)}")
            st.info("Make sure the file is in valid POTCAR format")


